def build_ngram_model(corpus: str, n: int) -> dict:
    """
    Build an N-gram language model with Laplace (Add-One) smoothing.

    Parameters
    ----------
    corpus : str  — raw input text
    n      : int  — order of the n-gram (e.g. 2 = bigram)

    Returns
    -------
    dict — {(w1,...,w_{n-1}): {next_word: smoothed_probability}}
    """
    text   = re.sub(r'[^\w\s]', '', corpus.lower())
    tokens = text.split()
    V      = len(set(tokens))          # vocabulary size

    context_counts = defaultdict(int)
    ngram_counts   = defaultdict(int)

    for i in range(len(tokens) - n + 1):
        ngram   = tuple(tokens[i : i + n])
        context = ngram[:-1]
        context_counts[context] += 1
        ngram_counts[ngram]     += 1

    # Laplace smoothing: P(w|ctx) = (count(ctx,w)+1) / (count(ctx)+V)
    model = defaultdict(dict)
    for ngram, cnt in ngram_counts.items():
        context   = ngram[:-1]
        next_word = ngram[-1]
        model[context][next_word] = round(
            (cnt + 1) / (context_counts[context] + V), 6
        )

    return dict(model)


text = "I love NLP and I love machine learning"
for n in [2, 3]:
    model = build_ngram_model(text, n)
    label = "Bigram" if n == 2 else "Trigram"
    print(f"\n{label} model (n={n}):")
    for ctx, probs in model.items():
        print(f"  Context {ctx}:")
        for word, prob in probs.items():
            print(f"    P({word!r:<12} | {ctx}) = {prob:.6f}")


