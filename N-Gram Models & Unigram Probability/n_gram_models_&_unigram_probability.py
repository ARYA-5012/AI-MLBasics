def compute_perplexity(train_corpus: str, test_sentence: str, n: int = 2) -> float:
    """
    Compute perplexity of a test sentence under an n-gram model
    trained on train_corpus, with Laplace smoothing.

    Perplexity = exp( -1/N * sum( log P(w_i | context) ) )
    """
    model       = build_ngram_model(train_corpus, n)
    train_clean = re.sub(r'[^\w\s]', '', train_corpus.lower()).split()
    V           = len(set(train_clean))

    test_tokens = re.sub(r'[^\w\s]', '', test_sentence.lower()).split()

    if len(test_tokens) < n:
        raise ValueError(f"Test sentence too short for n={n}.")

    log_prob_sum = 0.0
    num_ngrams   = 0

    for i in range(len(test_tokens) - n + 1):
        ngram   = tuple(test_tokens[i : i + n])
        context = ngram[:-1]
        word    = ngram[-1]

        if context in model and word in model[context]:
            prob = model[context][word]
        else:
            # Unseen context → uniform Laplace-smoothed fallback
            prob = 1 / V

        log_prob_sum += math.log(prob)
        num_ngrams   += 1
        print(f"  P({word!r:<10} | {context}) = {prob:.6f}  →  log = {math.log(prob):.4f}")

    perplexity = math.exp(-log_prob_sum / num_ngrams)
    return perplexity


